<h1>Настройки доступа к конфигурационному файлу</h1><br>
<p>Установка TicketHub требует доступ для записи в конфигурационный файл <strong>include/th-config.php</strong>. Please follow the instructions below to give read and write access to the web server.</p>
<h4>Shh/Telnet</h4>
<p>chmod 777 include/th-config.php</p>
<h4>FTP</h4>
<p>Using WS_FTP this would be right hand clicking on the file th-config.php , selecting chmod, and then giving all permissions to the file.</p>
<h4>Cpanel</h4>
<p>Нажмите на файл, выберите изменть права, и дайте все права на данный файл.</p>
