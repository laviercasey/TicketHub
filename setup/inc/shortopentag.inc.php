<h1>Для TicketHub необходимо, чтобы поддержка короткий тегов была включена</h1>
<p>Unfortunately TicketHub requires that <b>short_open_tag</b> be on. We plan on removing the requirement in the upcoming version(s). Our apologies for the inconvienence.</p>
<h2>Включите short_open_tag</h2>
<p>В зависимости от ваших прав на сервере - эту опцию можно настроить можно в php.ini, .htaccess или httpd.conf.</p>
  
